document.querySelector("#span1").addEventListener("click", function () {
    const pree1 = document.querySelector("#pre1");
    const spann1 = document.querySelector("#span1");
    if (pree1.style.display==='none') {
        spann1.innerHTML='&#8613;';
        pree1.style.display='block';
    }
    else {
        spann1.innerHTML='&#8615';
        pree1.style.display='none';
    }
});
